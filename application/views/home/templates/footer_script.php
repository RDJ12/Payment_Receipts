    $(document).on('click','.ask_detail',function(){
        $('#ask_modal').modal('show');
    });
    
    $(document).on('click','#submit_detail',function(){
        $("#form1").submit();
    });
    
    $("#form1").submit(function(event){
            event.preventDefault();
if($('#name').val()!='' && $('#email').val()!=''  && $('#phone').val()!='')
{
    
            $.ajax({
                    url:'<?=base_url();?>/Admin_Control/insert_user_detail',
                    type:'GET',
                    data:$(this).serialize(),
                    success:function(result){
                        if(result==1)
                        {
                            $('#form1').hide();
                            $('#form2').show();
                        }
                    }
            });
}            
        });
        
        $("#form2").submit(function(event){
            event.preventDefault();
if($('#otp').val()!='' )
{
    
            $.ajax({
                    url:'<?=base_url();?>/Admin_Control/check_otp',
                    type:'GET',
                    data:$(this).serialize(),
                    success:function(result){
                        if(result==2)
                        {   
                             $('#err').html('');
                            $('#form1').show();
                            $('#form2').hide();
                            $('#ask_modal').modal('hide');
                            window.location.reload();
                        }
                        else
                        {
                            $('#err').html('OTP Mismatch.');
                        }
                    }
            });
}            
        });
</script>